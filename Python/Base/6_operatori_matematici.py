# operatori matematici
# +, -, *, /



num1 = int(input('Scegli il primo numero '))
num2 = int(input('Scegli il secondo numero ' + '\n'))

print("=======ADDIZIONE=======\n")

risultato = num1 + num2
print(f'La somma dei 2 numeri è {risultato}')

print("=======SOTTRAZIONE=======\n")

risultato = num1 - num2
print(f'La differenza fra {num1} e {num2} è {risultato}')

print("=======MOLTIPLICAZIONE=======\n")

risultato = num1 * num2
print(f'Il prodotto di {num1} e {num2} è {risultato}')

print("=======DIVISIONE=======\n")

risultato = num1 / num2
print(f'Il quoziente di {num1} diviso {num2} è {risultato}')

print("=======POTENZA=======\n")

risultato = num1 ^ num2
print(f'La potenza di {num1} con esponente {num2} è {risultato}')